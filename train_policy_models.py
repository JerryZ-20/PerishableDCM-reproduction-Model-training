"""
train_policy_models.py

This script trains one machine learning model per product to predict the
best restock quantity.

High-level workflow:
1. Load the simulation dataset and configuration files.
2. Reconstruct the simulation state for each day from the logged dataset.
3. For each product and each day, evaluate several candidate restock quantities
   by replaying a short simulation rollout.
4. Label each row with the best-performing restock quantity.
5. Train a Random Forest classifier for each product separately.
6. Save the trained models, labeled training tables, and feature importances.

The goal is to learn a restocking policy that balances:
- profit / reward
- waste / scrapped inventory
- unmet demand
"""

import json
import os
from dataclasses import dataclass
from typing import Dict, List, Tuple

import joblib
import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, classification_report
from sklearn.model_selection import train_test_split

from managers import (
    CustomerManager,
    InventoryManager,
    ScenarioGeneratorRandom,
    StatManager,
    SupplyManager,
)
from envs import DailySimulation


# ---------------------------------------------------------------------
# File paths and training settings
# ---------------------------------------------------------------------

# Input dataset generated by the simulation.
DATASET_PATH = "./simulation_dataset.csv"

# Configuration files used to rebuild the simulation components.
PROD_CONFIG_PATH = "./configurations/conf_Products.json"
STORE_CONFIG_PATH = "./configurations/conf_Store.json"

# Folder where all trained artifacts will be stored.
MODEL_DIR = "./trained_models"

# We score candidate quantities using:
#   score = reward - LAMBDA_SCRAP * scrapped - LAMBDA_UNMET * unmet
#
# Increasing LAMBDA_SCRAP makes the labeler care more about waste.
# Increasing LAMBDA_UNMET makes the labeler care more about stockouts.
LAMBDA_SCRAP = 2.0
LAMBDA_UNMET = 1.0

# Number of Monte Carlo rollouts used when evaluating each candidate quantity.
# Larger values reduce noise but increase runtime.
N_ROLLOUTS = 3

# Fraction of rows held out for model evaluation.
TEST_SIZE = 0.25

# Random seed used for reproducibility in train/test split and model training.
RANDOM_STATE = 42


# ---------------------------------------------------------------------
# Data structure used to represent one reconstructed day of simulation
# ---------------------------------------------------------------------

@dataclass
class DayState:
    """
    Compact representation of the simulation state for one day.

    Attributes:
        day_idx:
            Absolute day number in the dataset.

        weekday:
            Day of week, typically 0-6.

        baseline_actions:
            Original restock quantities used in the logged dataset for this day,
            stored as {product_name: quantity}.

        inventory:
            Inventory arrays for each product at the start of the day,
            stored as {product_name: np.ndarray}.

        pipeline:
            On-order / in-transit arrays for each product at the start of the day,
            stored as {product_name: np.ndarray}.

        todays_demand:
            Reconstructed total demand for that day.
    """
    day_idx: int
    weekday: int
    baseline_actions: Dict[str, int]
    inventory: Dict[str, np.ndarray]
    pipeline: Dict[str, np.ndarray]
    todays_demand: int


# ---------------------------------------------------------------------
# Basic utility helpers
